        /// <summary>
        /// 1个月，31天.
        /// </summary>
        public static readonly int OneMonth = 31;

        /// <summary>
        /// 1年，366天.
        /// </summary>
        public static readonly int OneYear = 366;

        /// <summary>
        /// 3年，1096天.
        /// </summary>
        public static readonly int ThreeYears = 1096;

        /// <summary>
        /// 10年，3650天.
        /// </summary>
        public static readonly int TenYears = 3650;

        /// <summary>
        /// 永久，100年.
        /// </summary>
        public static readonly int Forever = 36500;

    }
}
